import type { NextPage } from 'next';
import Head from 'next/head';

import { NewsCardList, Pagination, NewsCardItem } from 'components/shared';
import { getCompanyNews } from 'api/company';

interface Props {
  news: any[];
}

const News: NextPage<Props> = ({ news }) => {
  const latestNewsItem = news[0];

  return (
    <div>
      <Head>
        <title>News</title>
        <meta name="description" content="Generated by create next app" />
      </Head>
      <div className="flex h-screen">
        <div className="h-1/2 mr-6">
          <NewsCardItem itemData={latestNewsItem} isLatest />
        </div>
        <div>
          <NewsCardList data={news} />
          <Pagination totalPages={24} perPageNumber={6} />
        </div>
      </div>
    </div>
  );
};

export const getServerSideProps = async () => {
  const from = '2022-04-01';
  const to = '2022-06-01';
  const companyNews = await getCompanyNews({ symbol: 'AAPL', from, to });
  return {
    props: {
      news: companyNews.filter((_, index) => index < 6),
    },
  };
};

export default News;
